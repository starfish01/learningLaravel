    public function createReply(Request $request)
    {
        //
        $user = Auth::user();

        $input = [
            'user_id'=> $user->id,
            'post_id'=> $request->post_id,
            'comment_id'=> $request->comment_id,
            'email'=>$user->email,
            'is_active'=> 1,
            'body'=>$request->body
        ];

        CommentReply::create($input);

        return redirect()->back();

    }

